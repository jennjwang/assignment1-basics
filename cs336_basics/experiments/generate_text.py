from cs336_basics.transformer.transformer import Transformer
import torch
from cs336_basics.modal_utils import VOLUME_MOUNTS, app, build_image
import json
'''
Using your decoder and your trained checkpoint, report the text generated by your model. You
may need to manipulate decoder parameters (temperature, top-p, etc.) to get fluent outputs.
Deliverable: Text dump of at least 256 tokens of text (or until the first <|endoftext|> token),
and a brief comment on the fluency of this output and at least two factors which affect how good
or bad this output is.
'''
@app.function(
    image=build_image(),
    gpu="H100",
    volumes=VOLUME_MOUNTS,
    timeout=60 * 60 * 3,
)
def generate_text(src, inputs, params, temperature=None, threshold=None, max_tokens=256, merges_path=None, input_path=None, special_tokens=None):

    model = Transformer(vocab_size=params["vocab_size"], 
                        context_length=params["context_length"], 
                        d_model=params["d_model"], 
                        num_layers=params["num_layers"], 
                        num_heads=params["num_heads"], 
                        d_ff=params["d_ff"], 
                        rope_theta=params["rope_theta"])

    model.to(params["device"])
    load_checkpoint(src, model, None)
    
    model = torch.compile(model)
    
    tokenizer = BPETokenizer.from_files(input_path, merges_path, special_tokens)
    tokens = tokenizer.encode(inputs)
    tokens = torch.tensor(tokens).unsqueeze(0)
    tokens = tokens.to(params["device"])

    outputs = decode(model, tokenizer, tokens, max_tokens, temperature=temperature, threshold=threshold)
    return tokenizer.decode(outputs.flatten().tolist())

